func TestMigratePartitions(t *testing.T) {
	// Variant A: separate tables with PartitionOf
	p := &Project{
		Schemas: []Schema{{
			Name: "public",
			Tables: []Table{
				{
					Name: "payment",
					Columns: []Column{
						{Name: "payment_id", Type: "integer"},
						{Name: "payment_date", Type: "timestamptz"},
					},
					PartitionBy: &PartitionBy{Type: "range", Columns: []ColRef{{Name: "payment_date"}}},
				},
				{
					Name:           "payment_p2024_01",
					PartitionOf:    "payment",
					PartitionBound: &PartitionBound{Value: "FOR VALUES FROM ('2024-01-01') TO ('2024-02-01')"},
					Columns: []Column{
						{Name: "payment_id", Type: "integer"},
						{Name: "payment_date", Type: "timestamptz"},
					},
				},
				{
					Name:           "payment_p2024_02",
					PartitionOf:    "payment",
					PartitionBound: &PartitionBound{Value: "FOR VALUES FROM ('2024-02-01') TO ('2024-03-01')"},
					Columns: []Column{
						{Name: "payment_id", Type: "integer"},
						{Name: "payment_date", Type: "timestamptz"},
					},
				},
			},
		}},
	}

	MigratePartitions(p)

	// Should have only 1 table (parent)
	assert.Len(t, p.Schemas[0].Tables, 1)
	parent := p.Schemas[0].Tables[0]
	assert.Equal(t, "payment", parent.Name)
	assert.Len(t, parent.Partitions, 2)
	assert.Equal(t, "payment_p2024_01", parent.Partitions[0].Name)
	assert.Equal(t, "FOR VALUES FROM ('2024-01-01') TO ('2024-02-01')", parent.Partitions[0].Bound)
	assert.Equal(t, "payment_p2024_02", parent.Partitions[1].Name)
}

