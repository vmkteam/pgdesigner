func TestGenerateDDL_SimpleTable(t *testing.T) {
	p := &Project{
		Schemas: []Schema{{
			Name: "public",
			Tables: []Table{{
				Name: "statuses",
				Columns: []Column{
					{Name: "statusId", Type: "integer", Nullable: "false", Identity: &Identity{Generated: "by-default"}},
					{Name: "title", Type: "varchar", Length: 255, Nullable: "false"},
					{Name: "alias", Type: "varchar", Length: 64, Nullable: "false"},
				},
				PK: &PrimaryKey{Name: "pk_statuses", Columns: []ColRef{{Name: "statusId"}}},
				Uniques: []Unique{{
					Name:    "statuses_alias_key",
					Columns: []ColRef{{Name: "alias"}},
				}},
			}},
		}},
	}

	ddl := GenerateDDL(p)

	assert.Contains(t, ddl, `CREATE TABLE "statuses"`)
	assert.Contains(t, ddl, `"statusId" integer NOT NULL GENERATED BY DEFAULT AS IDENTITY`)
	assert.Contains(t, ddl, `"title" varchar(255) NOT NULL`)
	assert.Contains(t, ddl, `"alias" varchar(64) NOT NULL`)
	assert.Contains(t, ddl, `PRIMARY KEY("statusId")`)
	assert.Contains(t, ddl, `CONSTRAINT "statuses_alias_key" UNIQUE("alias")`)
}

