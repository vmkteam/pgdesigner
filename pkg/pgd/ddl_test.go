func TestGenerateDDL_FK(t *testing.T) {
	p := &Project{
		Schemas: []Schema{{
			Name: "public",
			Tables: []Table{
				{
					Name: "statuses",
					Columns: []Column{
						{Name: "statusId", Type: "integer", Nullable: "false"},
					},
					PK: &PrimaryKey{Name: "pk_statuses", Columns: []ColRef{{Name: "statusId"}}},
				},
				{
					Name: "users",
					Columns: []Column{
						{Name: "userId", Type: "integer", Nullable: "false"},
						{Name: "statusId", Type: "integer", Nullable: "false"},
					},
					PK: &PrimaryKey{Name: "pk_users", Columns: []ColRef{{Name: "userId"}}},
					FKs: []ForeignKey{{
						Name:     "fk_users_statuses",
						ToTable:  "statuses",
						OnDelete: "restrict",
						OnUpdate: "restrict",
						Columns:  []FKCol{{Name: "statusId", References: "statusId"}},
					}},
				},
			},
		}},
	}

	ddl := GenerateDDL(p)

	// FK must be in ALTER TABLE, not in CREATE TABLE
	lines := strings.Split(ddl, "\n")
	var createUsers, alterUsers bool
	for _, line := range lines {
		if strings.Contains(line, `CREATE TABLE "users"`) {
			createUsers = true
		}
		if strings.Contains(line, `ALTER TABLE "users" ADD CONSTRAINT`) && strings.Contains(line, "FOREIGN KEY") {
			alterUsers = true
		}
	}
	assert.True(t, createUsers, "should have CREATE TABLE users")
	assert.True(t, alterUsers, "should have ALTER TABLE users ADD FK")

	// FK should NOT appear inside CREATE TABLE
	inCreate := false
	for _, line := range lines {
		if strings.Contains(line, `CREATE TABLE "users"`) {
			inCreate = true
		}
		if inCreate && strings.Contains(line, ");") {
			inCreate = false
		}
		if inCreate {
			assert.NotContains(t, line, "FOREIGN KEY", "FK should not be inline in CREATE TABLE")
			assert.NotContains(t, line, "REFERENCES", "REFERENCES should not be inline in CREATE TABLE")
		}
	}

	assert.Contains(t, ddl, `REFERENCES "statuses"("statusId")`)
	assert.Contains(t, ddl, "ON DELETE RESTRICT")
	assert.Contains(t, ddl, "NOT DEFERRABLE")
}

