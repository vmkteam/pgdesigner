func TestGenerateDDL_Golden(t *testing.T) {
	tests := []struct {
		name    string
		pgd     string
		golden  string
		tables  int
		fks     int
		indexes int
	}{
		{"chinook", "../format/sql/testdata/chinook.pgd", "../format/sql/testdata/chinook_generated.sql", 11, 11, 11},
		{"northwind", "../format/sql/testdata/northwind.pgd", "../format/sql/testdata/northwind_generated.sql", 14, 13, 0},
		{"pagila", "../format/sql/testdata/pagila.pgd", "../format/sql/testdata/pagila_generated.sql", 22, 18, 16},
		{"airlines", "../format/sql/testdata/airlines.pgd", "../format/sql/testdata/airlines_generated.sql", 8, 8, 0},
		{"adventureworks", "../format/sql/testdata/adventureworks.pgd", "../format/sql/testdata/adventureworks_generated.sql", 68, 89, 2},
	}

	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			data, err := os.ReadFile(tt.pgd)
			require.NoError(t, err)

			var p Project
			require.NoError(t, xml.Unmarshal(data, &p))

			ddl := GenerateDDL(&p)

			// golden file comparison
			want, err := os.ReadFile(tt.golden)
			require.NoError(t, err)
			assert.Equal(t, string(want), ddl, "DDL mismatch with golden file %s", tt.golden)

			// object counts
			assert.Equal(t, tt.tables, strings.Count(ddl, "CREATE TABLE "), "table count")
			assert.Equal(t, tt.indexes, strings.Count(ddl, "CREATE INDEX ")+strings.Count(ddl, "CREATE UNIQUE INDEX "), "index count")
			assert.Equal(t, tt.fks, strings.Count(ddl, "ALTER TABLE"), "FK count")

			// structure: CREATE TABLE before CREATE INDEX before ALTER TABLE
			createTablePos := strings.Index(ddl, "CREATE TABLE")
			createIndexPos := strings.Index(ddl, "CREATE INDEX")
			alterPos := strings.Index(ddl, "ALTER TABLE")

			if createIndexPos > 0 {
				assert.Less(t, createTablePos, createIndexPos, "CREATE TABLE should come before CREATE INDEX")
			}
			if alterPos > 0 {
				assert.Less(t, createTablePos, alterPos, "CREATE TABLE should come before ALTER TABLE")
			}
			if createIndexPos > 0 && alterPos > 0 {
				assert.Less(t, createIndexPos, alterPos, "CREATE INDEX should come before ALTER TABLE")
			}
		})
	}
}

