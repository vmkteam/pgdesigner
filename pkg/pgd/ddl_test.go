func TestGenerateDDL_PartitionByRange(t *testing.T) {
	p := &Project{
		Schemas: []Schema{{
			Name: "public",
			Tables: []Table{{
				Name: "payment",
				Columns: []Column{
					{Name: "payment_id", Type: "integer", Nullable: "false"},
					{Name: "amount", Type: "numeric", Precision: 5, Scale: 2, Nullable: "false"},
					{Name: "payment_date", Type: "timestamptz", Nullable: "false"},
				},
				PK: &PrimaryKey{Name: "pk_payment", Columns: []ColRef{{Name: "payment_id"}, {Name: "payment_date"}}},
				PartitionBy: &PartitionBy{
					Type:    "range",
					Columns: []ColRef{{Name: "payment_date"}},
				},
				Partitions: []Partition{
					{Name: "payment_p2024_01", Bound: "FOR VALUES FROM ('2024-01-01') TO ('2024-02-01')"},
					{Name: "payment_p2024_02", Bound: "FOR VALUES FROM ('2024-02-01') TO ('2024-03-01')"},
					{Name: "payment_default", Bound: "DEFAULT"},
				},
			}},
		}},
	}

	ddl := GenerateDDL(p)

	// Parent table should have PARTITION BY
	assert.Contains(t, ddl, `PARTITION BY RANGE ("payment_date")`)

	// Children should be CREATE TABLE ... PARTITION OF
	assert.Contains(t, ddl, `CREATE TABLE "payment_p2024_01" PARTITION OF "payment"`)
	assert.Contains(t, ddl, `FOR VALUES FROM ('2024-01-01') TO ('2024-02-01')`)
	assert.Contains(t, ddl, `CREATE TABLE "payment_p2024_02" PARTITION OF "payment"`)
	assert.Contains(t, ddl, `CREATE TABLE "payment_default" PARTITION OF "payment"`)
	assert.Contains(t, ddl, "DEFAULT")

	// Children should NOT be separate CREATE TABLE with columns
	assert.Equal(t, 1, strings.Count(ddl, `"payment_id" integer NOT NULL`), "columns should appear only once (in parent)")
}

