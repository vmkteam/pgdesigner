// --- Synthetic DDL validation against live PostgreSQL ---

func skipIfNoPG(t *testing.T) {
	t.Helper()
	cmd := exec.Command("psql", "postgres://postgres@localhost:5432/postgres?sslmode=disable", "-c", "SELECT 1")
	cmd.Stdout = nil
	cmd.Stderr = nil
	if err := cmd.Run(); err != nil {
		t.Skipf("PostgreSQL not reachable: %v", err)
	}
}

func psqlExec(t *testing.T, dsn, sql string) {
	t.Helper()
	cmd := exec.Command("psql", "--no-psqlrc", dsn, "-c", sql)
	out, err := cmd.CombinedOutput()
	if err != nil {
		t.Fatalf("psql failed: %v\n%s", err, out)
	}
}

func psqlFile(t *testing.T, dsn, file string) {
	t.Helper()
	cmd := exec.Command("psql", "--no-psqlrc", dsn, "-f", file)
	out, err := cmd.CombinedOutput()
	if err != nil {
		t.Fatalf("psql -f %s failed: %v\n%s", file, err, out)
	}
}

func psqlCount(t *testing.T, dsn, query string) int {
	t.Helper()
	cmd := exec.Command("psql", "--no-psqlrc", dsn, "-tAXc", query)
	out, err := cmd.Output()
	if err != nil {
		t.Fatalf("psql count failed: %v", err)
	}
	s := strings.TrimSpace(string(out))
	var n int
	for _, c := range s {
		if c >= '0' && c <= '9' {
			n = n*10 + int(c-'0')
		}
	}
	return n
}

// TestSynthetic_DDL_PostgreSQL loads synthetic.pgd, generates DDL, loads into live PG, verifies objects.
func TestSynthetic_DDL_PostgreSQL(t *testing.T) {
	skipIfNoPG(t)

	baseDSN := "postgres://postgres@localhost:5432/postgres?sslmode=disable"
	dbName := "pgd_test_synthetic"
	dsn := "postgres://postgres@localhost:5432/" + dbName + "?sslmode=disable"

	// Load pgd
	data, err := os.ReadFile("../../docs/testdata/synthetic/schema.pgd")
	require.NoError(t, err)

	var p Project
	require.NoError(t, xml.Unmarshal(data, &p))

	// Generate DDL
	ddl := GenerateDDL(&p)
	require.NotEmpty(t, ddl)

	// Write to temp file
	tmpFile := t.TempDir() + "/synthetic.sql"
	require.NoError(t, os.WriteFile(tmpFile, []byte(ddl), 0644))

	// Create DB
	psqlExec(t, baseDSN, "DROP DATABASE IF EXISTS "+dbName)
	psqlExec(t, baseDSN, "CREATE DATABASE "+dbName)
	defer psqlExec(t, baseDSN, "DROP DATABASE IF EXISTS "+dbName)

	// Load DDL — must succeed without errors
	psqlFile(t, dsn, tmpFile)

	// Verify object counts
	tables := psqlCount(t, dsn, "SELECT count(*) FROM pg_tables WHERE schemaname = 'public'")
	assert.Equal(t, 8, tables, "table count")

	indexes := psqlCount(t, dsn, `SELECT count(*) FROM pg_indexes WHERE schemaname = 'public' AND indexname NOT LIKE '%_pkey'`)
	assert.GreaterOrEqual(t, indexes, 15, "index count (excluding PKs)")

	domains := psqlCount(t, dsn, "SELECT count(*) FROM pg_type WHERE typtype = 'd' AND typnamespace = 'public'::regnamespace")
	assert.Equal(t, 3, domains, "domain count")

	enums := psqlCount(t, dsn, "SELECT count(*) FROM pg_type WHERE typtype = 'e' AND typnamespace = 'public'::regnamespace")
	assert.Equal(t, 2, enums, "enum count")

	functions := psqlCount(t, dsn, "SELECT count(*) FROM pg_proc p JOIN pg_namespace n ON p.pronamespace = n.oid WHERE n.nspname = 'public' AND p.prokind = 'f' AND p.proname IN ('update_user_search_vector','update_document_search_vector','audit_trigger_func')")
	assert.Equal(t, 3, functions, "function count")

	triggers := psqlCount(t, dsn, "SELECT count(*) FROM pg_trigger t JOIN pg_class c ON t.tgrelid = c.oid JOIN pg_namespace n ON c.relnamespace = n.oid WHERE NOT t.tgisinternal AND n.nspname = 'public'")
	assert.Equal(t, 4, triggers, "trigger count")

	fks := psqlCount(t, dsn, "SELECT count(*) FROM pg_constraint c JOIN pg_namespace n ON c.connamespace = n.oid WHERE c.contype = 'f' AND n.nspname = 'public'")
	assert.Equal(t, 9, fks, "FK count")

	checks := psqlCount(t, dsn, "SELECT count(*) FROM pg_constraint c JOIN pg_namespace n ON c.connamespace = n.oid WHERE c.contype = 'c' AND n.nspname = 'public'")
	assert.GreaterOrEqual(t, checks, 4, "CHECK constraint count")

	comments := psqlCount(t, dsn, "SELECT count(*) FROM pg_description d JOIN pg_class c ON d.objoid = c.oid JOIN pg_namespace n ON c.relnamespace = n.oid WHERE n.nspname = 'public'")
	assert.GreaterOrEqual(t, comments, 5, "comment count")
}

// TestSynthetic_ReferenceSQL_Match verifies that hand-written reference SQL creates
// the same objects as GenerateDDL output.
func TestSynthetic_ReferenceSQL_Match(t *testing.T) {
	skipIfNoPG(t)

	baseDSN := "postgres://postgres@localhost:5432/postgres?sslmode=disable"

	// Load reference SQL
	refDB := "pgd_test_synthetic_ref"
	refDSN := "postgres://postgres@localhost:5432/" + refDB + "?sslmode=disable"
	psqlExec(t, baseDSN, "DROP DATABASE IF EXISTS "+refDB)
	psqlExec(t, baseDSN, "CREATE DATABASE "+refDB)
	defer psqlExec(t, baseDSN, "DROP DATABASE IF EXISTS "+refDB)
	psqlFile(t, refDSN, "../../docs/testdata/synthetic/schema.sql")

	// Load generated DDL
	genDB := "pgd_test_synthetic_gen"
	genDSN := "postgres://postgres@localhost:5432/" + genDB + "?sslmode=disable"
	psqlExec(t, baseDSN, "DROP DATABASE IF EXISTS "+genDB)
	psqlExec(t, baseDSN, "CREATE DATABASE "+genDB)
	defer psqlExec(t, baseDSN, "DROP DATABASE IF EXISTS "+genDB)

	data, err := os.ReadFile("../../docs/testdata/synthetic/schema.pgd")
	require.NoError(t, err)
	var p Project
	require.NoError(t, xml.Unmarshal(data, &p))
	ddl := GenerateDDL(&p)
	tmpFile := t.TempDir() + "/synthetic_gen.sql"
	require.NoError(t, os.WriteFile(tmpFile, []byte(ddl), 0644))
	psqlFile(t, genDSN, tmpFile)

	// Compare object counts
	countQ := []struct{ name, query string }{
		{"tables", "SELECT count(*) FROM pg_tables WHERE schemaname = 'public'"},
		{"enums", "SELECT count(*) FROM pg_type t JOIN pg_namespace n ON t.typnamespace = n.oid WHERE t.typtype = 'e' AND n.nspname = 'public'"},
		{"domains", "SELECT count(*) FROM pg_type t JOIN pg_namespace n ON t.typnamespace = n.oid WHERE t.typtype = 'd' AND n.nspname = 'public'"},
		{"FK", "SELECT count(*) FROM pg_constraint c JOIN pg_namespace n ON c.connamespace = n.oid WHERE c.contype = 'f' AND n.nspname = 'public'"},
		{"functions", "SELECT count(*) FROM pg_proc p JOIN pg_namespace n ON p.pronamespace = n.oid WHERE n.nspname = 'public' AND p.prokind = 'f' AND p.proname IN ('update_user_search_vector','update_document_search_vector','audit_trigger_func')"},
		{"triggers", "SELECT count(*) FROM pg_trigger t JOIN pg_class c ON t.tgrelid = c.oid JOIN pg_namespace n ON c.relnamespace = n.oid WHERE NOT t.tgisinternal AND n.nspname = 'public'"},
	}
	for _, q := range countQ {
		ref := psqlCount(t, refDSN, q.query)
		gen := psqlCount(t, genDSN, q.query)
		assert.Equal(t, ref, gen, "%s count mismatch (ref=%d gen=%d)", q.name, ref, gen)
	}
}
